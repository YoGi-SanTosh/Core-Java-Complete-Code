package TypeCastingAndPolymorphism07March2025;

public class ConvertIntIntoByte {

	public static void main(String[] args) {

        int number = 256; // Example integer
        byte convertedByte = (byte) number; // Explicit type casting

        System.out.println("Original int: " + number);
        System.out.println("Converted byte: " + convertedByte);

	}

}
