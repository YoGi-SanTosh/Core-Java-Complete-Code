package LearnAutomationSeleniumWithJava;

public class OperatorsInJava16jan2024 {

	public static void main(String[] args) {
		
//initionaliaztion of variable
		int a=10;
		int b=20;
		
//additions two number's		
		int add=a+b;
		System.out.println(add);
		
//subtraction 
		
		int sub =a-b;
		System.out.println(sub);
		
//multiplication
		
		int multi=a*b;
		System.out.println(multi);
		
//division 
		int div =a/b;
		System.out.println(div);
		
//modulus
		
		int mod =a%b;
		System.out.println(mod);
		
		
	}

}
