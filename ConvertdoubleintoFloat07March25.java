package TypeCastingAndPolymorphism07March2025;

public class ConvertdoubleintoFloat07March25 {

	public static void main(String[] args)
	{
		double doubleValue = 123.456; // Example double value
		
        float floatValue = (float) doubleValue; // Explicit type casting

        System.out.println("Double Value: " + doubleValue);
        
        System.out.println("Converted Float Value: " + floatValue);
	}

}
